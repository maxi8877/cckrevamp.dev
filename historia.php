        <p>El proyecto del Centro Cultural Kirchner fue una iniciativa del expresidente de la Nación, Néstor Carlos Kirchner, y de la actual Presidenta, Cristina Fernández de Kirchner, quienes propusieron dotar a nuestro país de un gran espacio cultural
          capaz de albergar todas las expresiones del arte y la cultura, conservando la historia de un edificio emblemático como es el antiguo Palacio de Correos y Telégrafos. La idea era contar con un espacio moderno, amplio y diverso dedicado a las
          artes visuales, las artes escénicas, la educación, y demás manifestaciones artísticas, como parte de un proyecto político democrático que continuamente busca propiciar la inclusión, la participación popular y facilitar el acceso a los bienes
          culturales de toda la comunidad.
        </p>
        <p>En términos de infraestructura la obra tiene una relevancia trascendental, puesto que se trata de la recuperación y refuncionalización de uno de los edificios más importantes de Buenos Aires, el ex Palacio de Correos y Telégrafos (declarado Monumento
          Histórico Nacional en 1997 y cuyo mobiliario también fue reconocido como patrimonio histórico).
        </p>
        <p>El año 1880 fue un punto de inflexión en la historia del país, y uno de los principales hechos políticos fue la capitalización de la ciudad de Buenos Aires. A partir de ese momento y hasta la década de 1920, el Estado puso en marcha un ambicioso
          plan de obras públicas con el fin de materializar las instituciones de la Nación, y dotar a las grandes ciudades de la infraestructura necesaria para su desarrollo: escuelas, hospitales, puertos, oficinas administrativas y sedes gubernamentales.
          En ese contexto fue que el entonces presidente Miguel Juárez Celman nombra como Director de Correos y Telégrafos a Ramón J. Cárcano, quien llevó a cabo importantes reformas en el sistema postal del país, e impulsó la construcción de un “gran
          palacio” para la sede central del organismo a su cargo.
        </p>
        <p>El proyecto fue encargado al arquitecto francés Norbert-Auguste Maillart, que se instaló en Buenos Aires en 1887 y presentó una primera propuesta en 1888. Entre 1911 y 1912 se produjeron avances en las obras, pero una vez más fue modificado el
          proyecto: se quitaron las pasarelas perimetrales y se incorporó un gran esqueleto de acero que alivianaba el edificio y lo convertía en una de las obras más modernas del país.
        </p>
        <p>Los trabajos lograron finalizarse en 1928, año en que es inaugurado el edificio por el presidente Marcelo T. de Alvear.
        </p>
        <p>Si el proceso de construcción del antiguo Palacio de Correos duró 40 años, su restauración y remodelación –obra no menos compleja– llevó 5 años de intenso trabajo hasta llegar a convertirse en el Centro Cultural Kirchner que puede recorrerse al
          día de hoy. El proyecto evidencia el nuevo modelo de país que se inicia en el año 2003, y el modo en que la democratización de la cultura ocupa un rol central en este paradigma.
        </p>
        <p>El Centro Cultural Kirchner puede ser entendido como una pieza clave dentro del período más prolífico de nuestra historia con relación a la construcción y puesta en valor de espacios destinados a la cultura. Un ambicioso plan de obras, que se
          desarrolló en el marco de la conmemoración del Bicentenario, iniciado en el 2010 y que se proyecta más allá del año 2016.
        </p>
        <p>Los trabajos de restauración y puesta en valor incluyeron las cuatro fachadas del edificio y todo su interior; estas obras comenzaron en 2009 y abarcaron los amplios salones ceremoniales y la recuperación de sus luminarias, ascensores, teselas
          francesas, herrería y carpintería en general.
        </p>
        <p>También, se crearon nuevos espacios que posibilitan la presentación de una amplia y diversa oferta cultural. Entre sus principales atractivos se encuentran La Ballena Azul, una gran sala de música sinfónica para 1.750 espectadores; una Sala de
          Música de Cámara para 540 personas, y una estructura vidriada colgante que emula una Gran Lámpara –objeto que le da nombre–; sostenida desde un entramado de vigas Vierendeel.
        </p>
        <p>En los pisos 4to., 5to. y 6to., se transformaron áreas en desuso que antiguamente fueron oficinas, en nuevos espacios para reuniones, proyección de videos, conferencias, exposiciones y antesalas. Se construyeron 6 salas auditorios con capacidad
          para más de 104 espectadores cada una, salones de usos múltiples y vestíbulos que dan cuenta el esplendor del edificio. A su vez, en los pisos superiores (7mo.) se restauraron las carpinterías de la biblioteca y de la hemeroteca.
        </p>
        <p>La Cúpula: era un espacio residual que fue recuperado en el que se mantuvo la estructura general, pero se le reemplazaron las pizarras por un vidrio facetado que junto a un sistema lumínico computarizado (de luces de led) se convierte en un atractivo
          para la ciudad. Este lugar de usos múltiples cuenta con un escenario levadizo, luces escénicas, bancos laterales de pinotea y un exclusivo sistema de refrigeración que suma confortabilidad al lugar.
        </p>
        <p>Los miradores ubicados en la parte superior del Centro Cultural Kirchner son el primer punto panorámico público de Buenos Aires y ofrece una de las mejores vistas de la ciudad.
        </p>
        <p>El Centro Cultural Kirchner cuenta con más de 100.000 metros cuadrados y tiene la envergadura de los centros culturales más importantes del mundo, tales como L’ Auditori, de Barcelona; el Museo Nacional Centro de Arte Reina Sofía, de Madrid; el
          Tate Modern de Londres y el Centro Nacional de Arte y Cultura Georges Pompidou, de Paris.
        </p>
        <p>Es una obra clave para nuestro país, que permitirá que todos los argentinos puedan acceder a la cultura, a través de una programación amplia, diversa y de calidad, en un espacio pensado para tal fin, con una infraestructura a la altura de los
          principales centros culturales del mundo, y dotado de la última tecnología. Sin dudas, el Centro Cultural Kirchner no sólo será relevante para las generaciones presentes, también será un legado invaluable para las generaciones futuras.
        </p>



